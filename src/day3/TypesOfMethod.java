package day3;

public class TypesOfMethod {

	public static void main(String[] args) {
		//main method
		

	}
	
	public static void staticVoidMethod() {
		
		// static void method		
	}
	
	public void nonStaticVoidMethod() {
		
		// non Static void method
		
	}
	
	public static int staticReturnMethod( int x, int y) {
		return x+y;
		// return method
		
		
	}
	
	public int nonStaticReturnMethod(int x) {
		return x;
		//non static return method
	}
	
	public String returnMethod() {
		return "Khan";
		
		// non static return method
		
	}
	

}
